#ifndef _ALDABA_COOKIE_H
#define	_ALDABA_COOKIE_H

#define COOKIE_LEN 16

#define OP_CLIENT_INSERT    0x43  /* 'C' */
#define OP_CLIENT_RETRIEVE  0x52  /* 'R' */

#define OP_SERVER_INSERT    0x53  /* 'S' */
#define OP_SERVER_VERIFY    0x56  /* 'V' */

#define RESULT_SUCCESS      0x2b  /* '+' */
#define RESULT_FAILURE      0x2d  /* '-' */

#define COOKIE_SERVER_ADDR "localhost"
#define COOKIE_SERVER_ADDR_FAMILY AF_INET
#define COOKIE_SERVER_PORT 7177

typedef uint8_t u8;
typedef uint16_t u16;

typedef struct cookie{
    u8 cookie[COOKIE_LEN];
}cookie_t;

int server_insert_cookie(u8 *cookie);
int server_verify_cookie(u8 *cookie);
int client_insert_cookie(u8 *cookie);
u8 *client_retrieve_cookie();

#endif	/* _ALDABA_COOKIE_H */

